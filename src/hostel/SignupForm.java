    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
                                                    
  // Get user input from the text fields
  String username = txtUsername.getText().trim();
  String email = txtEmail.getText().trim();
  String password = new String(txtPassword.getPassword());

  // Basic check for empty fields
  if(username.isEmpty() || email.isEmpty() || password.isEmpty()) {
      javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields");
      return;
  }

  try {
      // Connect to the database
      Connection conn = DBConnection.getConnection();

      // Prepare SQL insert statement with placeholders
      String sql = "INSERT INTO users (username, email, password, role) VALUES (?, ?, ?, 'warden')";
      java.sql.PreparedStatement pst = conn.prepareStatement(sql);

      // Set values into the placeholders
      pst.setString(1, username);
      pst.setString(2, email);
      pst.setString(3, password);

      // Execute insert
      int inserted = pst.executeUpdate();

      if(inserted > 0) {
          javax.swing.JOptionPane.showMessageDialog(this, "Signup successful! Please login.");
          new LoginPage().setVisible(true);  // Open login page
          this.dispose();                    // Close signup page
      } else {
          javax.swing.JOptionPane.showMessageDialog(this, "Signup failed. Try again.");
      }

      pst.close();

  } catch (java.sql.SQLIntegrityConstraintViolationException ex) {
      // Duplicate email case
      javax.swing.JOptionPane.showMessageDialog(this, "Email already registered.");
  } catch (Exception e) {
      e.printStackTrace();
      javax.swing.JOptionPane.showMessageDialog(this, "Error connecting to database.");
  }

    }//GEN-LAST:event_btnSignupActionPerformed

